// Does Contain
template< typename T>
bool DoesContain( const T& item, const std::vector<T>& theVector )
{
	for(uint i = 0; i < theVector.size(); i++)
	{
		if(item == theVector.at(i))
			return true;
	}

	return false;
}


template< typename T>
bool DoesContain( const T& item, const std::vector<T*>& theVector )
{
	for(uint i = 0; i < theVector.size(); i++)
	{
		if(item == theVector.at(i))
			return true;
	}

	return false;
}

